# Os quatros tipos primitivos basicos em python são:
# 1_ int() é um tipo primitivo para converte para um número inteiro.
# 2_ float é um tipo primitivo para números reais ou com ponto flutuante ex: 1.2.
# 3_ bool  é um tipo primitivo para booleans/lógicos True or False Sempre iniciando com letra maiuscula ao usar um valor lógico.
# 4_ str   é um tipo primitivo para string/caracteres.

