# Comparação entre For e While

# For usado quando eu sei os valores d inicio e fim 
'''for c in range( 1, 10 +1):
    print(c)
print('Done')'''  

# While é usado quando eu não sei os valores d inicio e fim. Eu preciso descobrir o valor inicial e o valor final.
c = 0
while c <= 10:
  print(c)
  c += 1