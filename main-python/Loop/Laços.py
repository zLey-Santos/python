# Loop for para imprimir números de 0 a 5
for c in range(0, 6):  # O loop irá iterar sobre os números de 0 a 5
    print('Olá', c)  # Imprime 'Olá' seguido pelo valor de 'c'
print('Fim')  # Imprime 'Fim' após o término do loop

print('=' * 10)

# Loop for para imprimir números de 6 a 1 em ordem decrescente
for c in range(6, 0, -1):  # O loop irá iterar de 6 até 1, decrementando de 1 em 1
    print('Olá', c)  # Imprime 'Olá' seguido pelo valor de 'c'
print('Fim')  # Imprime 'Fim' após o término do loop

print('=' * 10)

# Loop for para imprimir números de 0 a 6, pulando de 2 em 2
for c in range(0, 7, 2):  # O loop irá iterar sobre os números de 0 a 6, ignorando o num 7, pulando de 2 em 2
    print('Olá', c)  # Imprime 'Olá' seguido pelo valor de 'c'
print('Fim')  # Imprime 'Fim' após o término do loop
